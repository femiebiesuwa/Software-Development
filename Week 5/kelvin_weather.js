// Conversion of 293K to Fahrenheit
const Kelvin = 293; // Declaration of temperature in kelvin as a constant and assigning a numeric value to it.
Celsius = Kelvin - 273; // This equation converts Kelvin to Celsius.
console.log(Celsius); // Output: 20

let Fahrenheit = Celsius * (9/5) + 32; // Declaration of Fahrenheit an the equation that converts Celsius to Fahrenheit.
console.log(Fahrenheit); // Output: 68

let fahrenheit = Fahrenheit; // Declaration of fahrenheit as another variable and assigning a value to it.
console.log(Math.floor(fahrenheit)); // Output: 68

let TEMPERATURE = fahrenheit;
console.log(`The temperature is ${TEMPERATURE} degrees in Fahrenheit.`) // Output: The temperature is 68 degrees in Fahrenheit.




